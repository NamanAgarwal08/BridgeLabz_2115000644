public class P5 { //ModulusOperator
    
    public static void main(String[] args) {
        
        int totPens = 14;
        int totStudents = 3;

        System.out.println("The Pen per Student is " + (totPens / totStudents)
                + " and the remaining pen not distributed is " + (totPens % totStudents));
    }
}
